#!/usr/bin/env node
/**
 * Generate the §9 ablation corpus from this repo's real exported symbols/files.
 *
 * Each task asks the model to write a minimal call to a REAL exported function
 * in a REAL file — so the hallucination guard can flag invented files/symbols.
 * Deterministic (index order + a fixed stride), so re-running is reproducible.
 *
 *   node scripts/gen-ablation-corpus.mjs            # write benchmarks/llm-ablation-tasks.json
 *   node scripts/gen-ablation-corpus.mjs --count 40
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { createRequire } from 'module';

const require = createRequire(import.meta.url);
const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const { buildSigIndex } = require(path.join(ROOT, 'src/retrieval/ranker.js'));

const countIdx = process.argv.indexOf('--count');
const COUNT = countIdx !== -1 && process.argv[countIdx + 1] ? parseInt(process.argv[countIdx + 1], 10) : 40;

/** Pull the function/def name out of a signature string. */
function fnName(sig) {
  const m = String(sig).match(/\b(?:async\s+function|function|def)\s+([A-Za-z_$][\w$]*)/);
  return m ? m[1] : null;
}

function slug(s) { return String(s).replace(/[^A-Za-z0-9]+/g, '-').replace(/^-|-$/g, '').toLowerCase(); }

// Collect (file, fn) candidates — one representative function per file, in index order.
const index = buildSigIndex(ROOT);
const candidates = [];
for (const [file, sigs] of index) {
  const rel = path.relative(ROOT, file).replace(/\\/g, '/');
  if (!rel.startsWith('src/') && !rel.startsWith('packages/')) continue;
  for (const sig of sigs || []) {
    const name = fnName(sig);
    if (name && !name.startsWith('_')) { candidates.push({ rel, name }); break; }
  }
}

// Even spread across the file list so tasks span many subsystems.
const picked = [];
const stride = Math.max(1, Math.floor(candidates.length / COUNT));
for (let i = 0; i < candidates.length && picked.length < COUNT; i += stride) picked.push(candidates[i]);
for (let i = 0; i < candidates.length && picked.length < COUNT; i++) {
  if (!picked.includes(candidates[i])) picked.push(candidates[i]);
}

const seen = new Set();
const tasks = picked.map(({ rel, name }) => {
  // Unique, informative id: call-<name>-<file-stem> (with a numeric fallback on collision).
  const stem = slug(rel.replace(/\.[^.]+$/, '').split('/').pop());
  let id = `call-${slug(name)}-${stem}`;
  while (seen.has(id)) id += '-x';
  seen.add(id);
  return {
    id,
    prompt: `Write a minimal Node.js example that requires \`${rel}\` and calls its \`${name}\` function with correct arguments. Reference only real files, exports, and symbols from this repository — do not invent helpers, paths, or parameters.`,
  };
});

const out = path.join(ROOT, 'benchmarks', 'llm-ablation-tasks.json');
const payload = {
  _comment: `LLM A/B hallucination ablation corpus (IMPL §9), generated by scripts/gen-ablation-corpus.mjs from real repo symbols. ${tasks.length} tasks. Run: npm run benchmark:llm-ablation (needs ANTHROPIC_API_KEY or GEMINI_API_KEY).`,
  tasks,
};
fs.writeFileSync(out, JSON.stringify(payload, null, 2) + '\n');
console.log(`[gen-ablation-corpus] wrote ${tasks.length} tasks → benchmarks/llm-ablation-tasks.json`);
